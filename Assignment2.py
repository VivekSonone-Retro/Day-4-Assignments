# Take two numbers from the user
a = float(input("Enter first number: "))
b = float(input("Enter second number: "))

# Compare using all comparison operators
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)
